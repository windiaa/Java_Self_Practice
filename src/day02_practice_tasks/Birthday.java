package m10_variables_data_types;

public class Birthday {

    public static void main(String[] args) {

        String name = "John";
        int birthDay = 25;
        String birthMonth = "April";
        int birthYear = 1995;

        System.out.println(name + " was born on " + birthMonth + " " + birthDay + ", " + birthYear + ".");

    }
}
