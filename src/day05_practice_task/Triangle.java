package day05_practice_task;

public class Triangle {
    public static void main(String[] args) {


        int rows = 10;

        for (int i = 1; i <= rows; i++) {
            System.out.println();

            for (int j = 1; j <= i; j++) {
                System.out.print("* ");

            }
        }
    }
    
   
    
}
